package taller3.televisores;

public class Marca {
	
	private String nombre;
	
	
	//constructor nombre 
	public Marca (String nombre) {
		this.nombre = nombre;
	}
	
	
	//metodo set atributo nombre
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	//metodo get atributo nombre
	public String getNombre() {
		return nombre;
	}
		

}
